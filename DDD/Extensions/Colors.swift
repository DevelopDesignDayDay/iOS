extension UIColor {
    class var dddGray: UIColor {
        return UIColor(red: 51/255.0, green: 51/255.0, blue: 51/255.0, alpha: 1.0)
    }
    class var dddLightGray: UIColor {
        return UIColor(red: 102/255.0, green: 102/255.0, blue: 102/255.0, alpha: 1.0)
    }
    class var dddRed: UIColor {
        return UIColor(red: 239/255.0, green: 48/255.0, blue: 36/255.0, alpha: 1.0)
    }
    class var dddBlack: UIColor {
        return UIColor(red: 21/255.0, green: 21/255.0, blue: 21/255.0, alpha: 1.0)
    }
    class var dddButtonGray: UIColor {
        return UIColor(red: 153/255.0, green: 153/255.0, blue: 153/255.0, alpha: 1.0)
    }
}
